function [u1,u2,K] = RungeKutta4(K,fu1,fu2,u1,u2,N)
    global a b h
    u1(1) = u1;
    u2(1) = u2;
    % %================
    x = a:h:b;
    % %================
    for i = 1:N
        K(1,1)  = fu1(x(i),u1(i),u2(i));
        K(1,2)  = fu2(x(i),u1(i),u2(i));
        
        K(2,1)  = fu1(x(i)+h/2,u1(i)+K(1,1)*h/2,u2(i)+K(1,2)*h/2);
        K(2,2)  = fu2(x(i)+h/2,u1(i)+K(1,1)*h/2,u2(i)+K(1,2)*h/2);
    
        K(3,1)  = fu1(x(i)+h/2,u1(i)+K(2,1)*h/2,u2(i)+K(2,2)*h/2);
        K(3,2)  = fu2(x(i)+h/2,u1(i)+K(2,1)*h/2,u2(i)+K(2,2)*h/2);
        
        K(4,1)  = fu1(x(i+1),u1(i)+h*K(3,1),u2(i)+h*K(3,2));
        K(4,2)  = fu2(x(i+1),u1(i)+h*K(3,1),u2(i)+h*K(3,2));
    
        u1(i+1) = u1(i)+(K(1,1)+2*K(2,1)+2*K(3,1)+K(4,1))*(h/6);
        u2(i+1) = u2(i)+(K(1,2)+2*K(2,2)+2*K(3,2)+K(4,2))*(h/6);
    end
end